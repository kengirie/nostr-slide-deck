import '@testing-library/jest-dom';
import { vi } from 'vitest';

// Node 25+ injects its own experimental Web Storage (backed by --localstorage-file),
// which shadows jsdom's implementation and throws in tests. Replace it with a
// working in-memory Storage.
function createMemoryStorage(): Storage {
  const store = new Map<string, string>();
  return {
    get length() {
      return store.size;
    },
    clear: () => store.clear(),
    getItem: (key: string) => store.get(key) ?? null,
    key: (index: number) => Array.from(store.keys())[index] ?? null,
    removeItem: (key: string) => {
      store.delete(key);
    },
    setItem: (key: string, value: string) => {
      store.set(key, String(value));
    },
  };
}

for (const target of [globalThis, window]) {
  Object.defineProperty(target, 'localStorage', { writable: true, value: createMemoryStorage() });
  Object.defineProperty(target, 'sessionStorage', { writable: true, value: createMemoryStorage() });
}

